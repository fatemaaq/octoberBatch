package Basic;

//Java session 3 P1
public class AssigmentNestedFor {
	public static void main(String[] args) {
		for (int i = 1; i <= 5; i++) {

			System.out.println("12345");
		}

		System.out.println();

		
		// Java session 3 P2
		for (int i = 1; i <= 5; i++) {
			for (int j = 1; j <= 5; j++) {

				System.out.print(i);
			}

			System.out.println();
		}
		
		System.out.println();
		
			
		// Java session 3 P3
		for (int i = 1; i <= 5; i++) {
			for (int j = 1; j <= i; j++) {

				System.out.print(j);

			}
			System.out.println();
		
		}
		System.out.println();
		
		
		// Java session 3 P4
		for(int i = 5; i>0; i--) {
				for(int j = 1; j<=i; j++) {
					System.out.print(j);
				}
				
			 System.out.println();
			
	}
		
}
}

